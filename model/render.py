def render_classifier_layer(
    TermBits,
    TermCount,
    BusBits,
    InChannels,
    ClassCount,
    WeightBits,
    BiasBits,
    Weights,
    Biases,
    instance,
):
    # Classifier reads from the final linear/conv stage
    valid_i = f"conv_{instance - 1}_valid"
    data_i  = f"conv_{instance - 1}_data"
    
    # --- Classifier drives the dedicated ready wire ---
    ready_o = "classifier_ready"

    # Classifier drives the global output handshake
    ready_i = "ready_i"
    valid_o = "valid_o"
    
    # FIXED: Map the classifier's class_o to the top-level data_o port
    class_o = "data_o" 

    lines = [
        "  classifier_layer #(",
        f"     .TermBits   ({TermBits})",
        f"    ,.TermCount  ({TermCount})",
        f"    ,.BusBits    ({BusBits})",
        f"    ,.InChannels ({InChannels})",
        f"    ,.ClassCount ({ClassCount})",
        f"    ,.WeightBits ({WeightBits})",
        f"    ,.BiasBits   ({BiasBits})",
        f"    ,.Weights    ({Weights})",
        f"    ,.Biases     ({Biases})",
        f"  ) classifier_layer_inst_{instance} (",
        "     .clk_i   (clk_i)",
        "    ,.rst_i   (rst_i)",
        "",
        f"    ,.ready_o  ({ready_o})",
        f"    ,.valid_i  ({valid_i})",
        f"    ,.data_i   ({data_i})",
        "",
        f"    ,.valid_o  ({valid_o})",
        f"    ,.ready_i  ({ready_i})",
        f"    ,.class_o  ({class_o})",
        "  );\n",
    ]
    return "\n".join(lines)


